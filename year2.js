var card1 = document.querySelector(".year1");
card1.addEventListener("mousemove",function(){
   anime({
      targets: document.querySelector(".year1"),
      rotateY:"+=180",
      easing:'easeInOutSine',
      duration:400,
    });
})

var card1 = document.querySelector(".year1");
